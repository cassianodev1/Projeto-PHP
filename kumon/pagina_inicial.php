            <h1>O que é Zyon?</h1>
            <p>Zyon é um método que visa desenvolver o aprendizado dos alunos de forma individualizada por intermédio das disciplinas de matemática e programação. </p>

            <h1>No Zyon, o próprio aluno desenvolve sua capacidade</h1>
            <p>A grande inovação do método Zyon está no fato de não se ensinar ao aluno como resolver o material proposto. Ele é estruturado de forma a fazer com que o aluno solucione os exercícios sozinho.
                Independente de idade ou da série escolar em que seu filho esteja, ele começará os estudos por questões simples para, aos poucos, passar a ter contato com questões mais complexas, sempre adequadas à sua capacidade. Desta forma ele terá sempre condições de alcançar a nota 100 (nota máxima). O objetivo é desenvolver no aluno a autoconfiança, o interesse em estudar e aprender por si até que consiga chegar ao seu desempenho máximo. Assim, em cada estudo realizado, ele poderá sentir a satisfação e dizer para si mesmo: “Eu consegui!”, experimentando a alegria de aprender e de expandir, cada vez mais, sua própria capacidade.</p>
